@XmlRootElement(name = "orders")
public class Orders
{
   protected Collection<Order> orders = new ArrayList<Order>();

   @XmlElementRef
   public Collection<Order> getOrders()
   {
      return orders;
   }

   public void setOrders(Collection<Order> orders)
   {
      this.orders = orders;
   }
}
