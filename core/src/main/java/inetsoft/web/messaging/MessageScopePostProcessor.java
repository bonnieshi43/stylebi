package inetsoft.web.messaging;

import org.springframework.beans.BeansException;
import org.springframework.beans.factory.config.BeanFactoryPostProcessor;
import org.springframework.beans.factory.config.ConfigurableListableBeanFactory;

/**
 * Bean factory post-processor that registers the message scope.
 */
public class MessageScopePostProcessor implements BeanFactoryPostProcessor {
   @Override
   public void postProcessBeanFactory(ConfigurableListableBeanFactory factory)
      throws BeansException
   {
      factory.registerScope("message", new MessageScope());
   }
}
