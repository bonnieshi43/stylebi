package inetsoft.web.viewsheet.command;

import com.fasterxml.jackson.databind.annotation.JsonSerialize;
import org.immutables.value.Value;

@Value.Immutable
@JsonSerialize(as = ImmutableAnnotationChangedCommand.class)
public abstract class AnnotationChangedCommand implements ViewsheetCommand {
   // if the annotation has changed and should prompt the user to save their changes
   // if they try to close the viewsheet
   @Value.Parameter abstract boolean isChanged();

   public static AnnotationChangedCommand of(final boolean changed) {
      return ImmutableAnnotationChangedCommand.of(changed);
   }
}
