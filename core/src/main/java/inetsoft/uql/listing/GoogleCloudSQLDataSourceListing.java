package inetsoft.uql.listing;

import inetsoft.uql.DataSourceListing;
import inetsoft.uql.XDataSource;
import inetsoft.uql.jdbc.JDBCDataSource;

public class GoogleCloudSQLDataSourceListing extends DataSourceListing {
   public GoogleCloudSQLDataSourceListing() {
      super("Google Cloud SQL", "Relational Database", "/inetsoft/uql/listing/google-cloudsql.svg");
   }

   @Override
   public XDataSource createDataSource() throws Exception {
      final JDBCDataSource ds = new JDBCDataSource();
      ds.setName(getAvailableName());
      ds.setURL("jdbc:mysql:///<DATABASE_NAME>?cloudSqlInstance=<INSTANCE_CONNECTION_NAME>&socketFactory=com.google.cloud.sql.mysql.SocketFactory&useSSL=false&user=<MYSQL_USER_NAME>&password=<MYSQL_USER_PASSWORD>");
      ds.setDriver("com.mysql.jdbc.Driver");
      ds.setCustom(true);

      return ds;
   }
}
